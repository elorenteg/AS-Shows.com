package showscom.domainLayer.domainModel;

public class Seient {
	private int fila;
	private int columna;

	public Seient(int fila, int columna) {
		super();
		this.fila = fila;
		this.columna = columna;
	}

	public int getFila() {
		return fila;
	}

	public int getColumna() {
		return columna;
	}

}
